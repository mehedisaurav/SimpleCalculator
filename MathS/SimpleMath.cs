namespace MathS
{
    public class SimpleMath
    {
        public  int Add(int val1, int val2)
        {
            return (val1 + val2);
        }

        public  int Substraction(int val1, int val2)
        {
            return (val1 - val2);
        }

        public  int Multiple(int val1, int val2)
        {
            return (val1 * val2);
        }

        public  int Division(int val1, int val2)
        {
            return (val1 / val2);
        }

    }
}